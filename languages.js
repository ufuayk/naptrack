let currentLang = localStorage.getItem('sleepAppLang') || 'en';

function setLanguage(lang) {
    currentLang = lang;
    localStorage.setItem('sleepAppLang', lang);
    
    document.querySelectorAll('.lang-btn').forEach(btn => {
        btn.classList.toggle('active', btn.dataset.lang === lang);
    });
    
    updatePageText();
}

function getText(key) {
    return translations[currentLang][key];
}

function updatePageText() {
    document.querySelector('.header h2').textContent = getText('title');
    
    document.querySelector('#startButton').innerHTML = `
        <i class="fas fa-moon"></i>
        ${getText('sleepButton')}
    `;
    document.querySelector('#endButton').innerHTML = `
        <i class="fas fa-sun"></i>
        ${getText('wakeButton')}
    `;
    
    document.querySelector('.footer p').textContent = getText('copyright');
    document.querySelector('.btn-reset span').textContent = getText('resetData');
    
    updateUI();
}